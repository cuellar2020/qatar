        ->orderBy('nombre', 'asc')
        ->join('pais','pais.id', '=','sede.pais')
        ->select('sede.imagen as imagen','sede.nombre as nombre','pais.imagen as pais','pais.nombre as nombrep')
        ->get();

        return view('usuario.sedes')->with('sedes',$sedes);
